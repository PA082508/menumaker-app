--
-- ✅ ПРИМЕНЕНА 2026-07-16 по слову Николая («одобрение образца, Контрподпись
--    директора и Потенциальный + факт сбора»).
--
-- READ-BACK (фактический):
--   enrollment_submissions → 72 строки, не тронуты
--   fee_received_at is not null → 0
--   статусы → approved 18 · pending 4 · rejected 50 (не сдвинулись)
--   Вьюх над enrollment_submissions НЕТ (проверено pg_depend) → ломать нечего.
--
-- ЗАЧЕМ
-- ─────
-- Николай, 2026-07-16, описал свой порядок зачисления:
--   осмотр садика → пакет №1 (Consent + Start form) с оплатой регистрационных
--   → выдаём QR на пакет №2/№3. «которые не оплатили хотели бы иметь их
--   потенциальными».
--
-- То есть сбор — это развилка: заплатил → зачисляется; не заплатил →
-- ПОТЕНЦИАЛЬНЫЙ, пакет №2 не выдаём.
--
-- ЧЕГО ЗДЕСЬ НЕТ И ПОЧЕМУ
-- ───────────────────────
-- ЭТО НЕ ПЛАТЁЖНАЯ СИСТЕМА. Николай сам её заморозил: «платёжная система
-- немножко подождёт вместе с экономикой пока мы не завершим работу с программой
-- питание». Оплата со смартфона на смартфон — это она и есть, и её тут нет.
-- Замерено: таблиц оплаты в БД ноль (payment/fee/invoice/tuition/billing).
--
-- Здесь — ОДИН ФАКТ: «сбор получен», который директор отмечает рукой. Факту
-- платёжная система не нужна. Когда она появится, галочка просто начнёт
-- ставиться сама, а это место не поменяется.
--
-- ПОЧЕМУ НЕ НОВАЯ ТАБЛИЦА И НЕ НОВЫЙ СТАТУС
-- ─────────────────────────────────────────
-- Потенциальный УЖЕ существует: это залоченное решение «Ничего не пишется в
-- roster до Approve» (Enrollment_Approval_Loop_Spec, Фаза 1). Заполнил, но не
-- одобрен → живёт в enrollment_submissions, в ростер не попал. Consent Sonia
-- Texidor прямо сейчас такая запись: pending, child_id пуст.
--
-- Новый статус ('potential') тоже не заводим: статус — исход рассмотрения
-- (pending/approved/rejected/received), а сбор — независимый факт о деньгах.
-- Смешать их значит потерять возможность сказать «оплатил, но ещё не рассмотрен».
-- Поэтому две колонки, ортогональные статусу.
--
-- ГДЕ ИМЕННО. Сбор — про СЕМЬЮ, а не про бумагу, но семьи как записи ещё нет
-- (в том и суть потенциального). Носитель — заявка `start_form`: это форма
-- пакета №1, которая несёт данные ребёнка и, по словам Николая, оплату.
--
--   потенциальный := start_form · status='pending' · fee_received_at is null
--   зачисляется   := start_form · fee_received_at is not null
--
-- Approve НЕ блокируем сбором: директор вправе принять ребёнка и без него
-- (льгота, перенос, своя причина). Экран показывает факт, решение — за человеком.

begin;

alter table menumaker.enrollment_submissions
  add column if not exists fee_received_at timestamptz,
  add column if not exists fee_received_by uuid;

comment on column menumaker.enrollment_submissions.fee_received_at is
  'Регистрационный сбор получен (Николай, 2026-07-16). NULL на start_form = ПОТЕНЦИАЛЬНЫЙ: '
  'заполнил пакет №1, но не приступил — пакет №2/№3 не выдаём. Это ФАКТ, отмечаемый рукой, '
  'а НЕ платёжная система: она заморожена владельцем до рабочего уровня питания. Когда '
  'появится — галочка начнёт ставиться сама, место не изменится. Ортогонально status: '
  '«оплатил, но ещё не рассмотрен» обязано быть выразимо.';

comment on column menumaker.enrollment_submissions.fee_received_by is
  'Кто отметил получение сбора (auth.uid директора). Вместе с fee_received_at — весь след.';

commit;

